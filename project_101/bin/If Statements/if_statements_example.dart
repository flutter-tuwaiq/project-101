/// ----------- Question -------------------
/// What is an if statement in Dart?
/// Provide an example of when you would use an if statement
/// ------------  Answer  --------------------
///
/// an if statement is a control flow statement
///  that allows you to execute a block of code if a certain condition is true.
/// -----------  EXAMPLE  --------------------
/// Suppose you want to write a program
/// that checks if a number is positive or negative

void main() {
  int number = 5;

  if (number > 0) {
    print("The number is positive.");
  } else {
    print("The number is negative.");
  }
}
